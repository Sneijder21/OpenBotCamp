package company;

public class _01Tips {

    public static void main(String[] args) {

        // tipo identificador = valor;
        // tipo identificador;
        // identificador = valor;

        // enteros
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 byte
        int number3  = 3; // 4 byte
        long number4 = 4L; // 8 byte

        // punto flotante
        float decimal1 = 4.9f;
        double decimal2 = 9.99d;

        // caracter
        char caracter1 = 'a';

        // booleanos
        boolean verdadero = true;
        boolean falso = false;

        // cadenas de texto
        String nombre = "Alan";
        String apellido = "Lopez";

        // Int tipos primitivos.
        // Int cannot be null, but Integer is a class is possible set value null.

        // tipos envoltorio
        Integer numero = null;
        Long numero2 = 2L;

        // palabras reservadas, search keyword java;
        // first letter in mayuscula for classes, first letter minuscula for variable.
        var i = 4;
        System.out.println(i);
    }
}